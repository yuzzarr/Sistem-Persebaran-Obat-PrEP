                                <p class="text-justify"><b>Obat PrEP (pre-exposure prophylaxis)</b> adalah obat pencegah penularan infeksi bagi orang-orang yang berisiko tinggi tertular HIV. Meski bertujuan untuk mencegah HIV, kemanjuran obat ini diperkirakan hanya sekitar 92% untuk menurunkan risiko tertular HIV.</p>
